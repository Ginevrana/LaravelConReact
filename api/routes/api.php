Route::middleware('auth:sanctum')->get('/user', function (Request $request) {
    return $request->user();
});

// Si un grupo de rutas utiliza el mismo controlador puede usar el método del controlador para definir el controlador común para todas las rutas dentro del grupo. Luego de definir las rutas solo necesita proporcionar el método del controlador que invocan.
Route::controller(ProductController::class)->group(function(){
    Route::get('/products', 'index');
    Route::post('/product', 'store');
    Route::get('/product/{id}', 'show');
    Route::put('/product/{id}', 'update');
    Route::delete('/product/{id}', 'destroy');
});